    public void displaySortedNumbers(int[] sortedNumbers, String sortMethod) {
        StringBuilder result = new StringBuilder("Ordered numbers using " + sortMethod + ": ");
        for (int num : sortedNumbers) {
            result.append(num).append(" ");
        }
        jLabel5.setText(result.toString());
    }

