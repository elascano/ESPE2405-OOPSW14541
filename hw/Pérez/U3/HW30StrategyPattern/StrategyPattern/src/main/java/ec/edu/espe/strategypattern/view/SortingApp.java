    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // Obtener cantidad de números
            int n = Integer.parseInt(jTextPane1.getText().trim());
            if (n <= 0) {
                JOptionPane.showMessageDialog(this, "Enter a positive number.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Obtener los números
            String[] inputNumbers = jTextArea1.getText().trim().split("\\s*,\\s*");
            if (inputNumbers.length != n) {
                JOptionPane.showMessageDialog(this, "Number of inputs does not match the specified count.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int[] numbers = new int[n];
            for (int i = 0; i < n; i++) {
                try {
                    numbers[i] = Integer.parseInt(inputNumbers[i]);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Invalid number format.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            // Usar el controlador para ordenar
            sortingController.sortAndDisplay(numbers, this);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid input format.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

