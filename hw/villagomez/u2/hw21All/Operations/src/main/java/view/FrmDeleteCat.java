    private void loadCatsToTable() {
        List<Cat> cats = MongoDB.getCats();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data

        for (Cat cat : cats) {
            model.addRow(new Object[]{
                cat.getId(),
                cat.getName(),
                cat.getBreed(),
                cat.getAge()
            });
        }
    }

    private void deleteSelectedCat() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String catId = jTable1.getValueAt(selectedRow, 0).toString();
            String catName = jTable1.getValueAt(selectedRow, 1).toString();

            int confirm = JOptionPane.showConfirmDialog(this,
                    "Are you sure you want to delete the cat: " + catName + "?",
                    "Confirm Deletion",
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                boolean deleted = MongoDB.deleteCatById(catId);
                if (deleted) {
                    JOptionPane.showMessageDialog(this, "Cat deleted successfully.");
                    loadCatsToTable(); // Reload the table
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete cat.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a cat to delete.", "No Selection", JOptionPane.WARNING_MESSAGE);
        }
    }

