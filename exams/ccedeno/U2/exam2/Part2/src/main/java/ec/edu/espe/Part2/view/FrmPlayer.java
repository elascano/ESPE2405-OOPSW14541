    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int id = Integer.parseInt(txtId.getText());
    String name = txtName.getText();
    String descripcion = txtDescripcion.getText();
    int goles = Integer.parseInt(txtGoal.getText());
    int asistencias = Integer.parseInt(txtAsist.getText());
    
    
    double promedioGA = calcularPromedioGA(goles, asistencias);
    String str = String.valueOf(promedioGA);
    
    txtGa.setText(str);
    Player player = new Player(id, name, descripcion, goles, asistencias,(int) promedioGA);
    
  
    String message = String.format("Jugador: %s\nID: %d\nDescripción: %s\nGoles: %d\nAsistencias: %d\nPromedio GA por partido: %.2f", 
                                    player.getName(), player.getId(), player.getDescription(), player.getGoal(), player.getAsist(), promedioGA);
    
    javax.swing.JOptionPane.showMessageDialog(this, message);
    
    savePlayer(player);
    }//GEN-LAST:event_btnSaveActionPerformed

