        public void updateTableOfMenus(){
            List<Document> documents = mongoDB.getMenuInformation();

            DefaultTableModel model = (DefaultTableModel) tabla.getModel();
            model.setRowCount(0);

        for (Document doc : documents) {
               int id =doc.getInteger("Id");
               String name=doc.getString("Name");
               double value =doc.getDouble("Value");

                // Si necesitas convertir a float, hazlo aquí
               float valueFloat = (float) value;
     

    // Añade una fila al modelo con los valores
    model.addRow(new Object[]{id,name,valueFloat});
        }
    }
