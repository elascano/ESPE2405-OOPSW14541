    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        
            Park product;
            //int id = generarId();
            int id = Integer.parseInt(jTextField1.getText());
            String name = jTextField2.getText();
            float value = Float.parseFloat(jTextField3.getText());

            //crea un objeto
            product = new Park(id, name, value);

            //inseta en la basae de datos
            mongoDB.createPark(product);

            JOptionPane.showMessageDialog(rootPane,"Producto Agregado");

            // Actualizar la tabla
            DefaultTableModel model = (DefaultTableModel) tabla.getModel();
            model.addRow(new Object[]{product.getId(), product.getName(), product.getValue()});
         }
         catch (NumberFormatException e) {
        // Manejar el caso en que la conversión del texto a float falla
        JOptionPane.showMessageDialog(rootPane, "Error al registrar");
    }

        
    }//GEN-LAST:event_jButton1ActionPerformed

