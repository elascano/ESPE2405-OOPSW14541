package ec.edu.espe.model;

/**
 *
 * @author G406
 */
public class Park {

    private int id;
    private String name;

    public Park(int id, String name, float value) {
        this.id = id;
        this.name = name;
        this.value = value;
    }
    private float value;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the value
     */
    public float getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(float value) {
        this.value = value;
    }
}
