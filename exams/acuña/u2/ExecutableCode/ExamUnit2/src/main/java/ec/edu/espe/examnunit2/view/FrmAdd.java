    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
   
        String id = txtId.getText().trim();
        String brand = txtBrand.getText().trim();
        String color = txtColor.getText().trim();
        String year = txtYear.getText().trim();
        

        ControllerVehicle vehicleController = new ControllerVehicle();
        
   
        double price = vehicleController.calculatePrice(year);
        

        lblPrice.setText(String.format("$%.2f", price)); 

        
        Vehicle vehicle = new Vehicle(id, brand, color, Integer.parseInt(year));
        vehicle.setPrice(price);

        try {
            
            MongoDBUtils.saveVehicle(id, brand, color, Integer.parseInt(year),price); 
            JOptionPane.showMessageDialog(null, "Vehiculo SAVED.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something goes wrong: " + e.getMessage());
        }
    
    }//GEN-LAST:event_btnAddActionPerformed

