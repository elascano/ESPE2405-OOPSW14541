    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        float pbi1 = Float.parseFloat(txtpbi1.getText());
        float pbi2 = Float.parseFloat(txtpbi2.getText());
        String result =  PbiCalculator.computeDifferencePbi(pbi2, pbi2);
        int id = parseInt(txtname.getText());
        String name = txtname.getText();
        
        txtResult.setText(String.valueOf(result));
        float result2  = Float.parseFloat(result);
        
        Countries countries = new Countries(id, name, pbi1, pbi2, result2);
        utils.MongoDBUtil.create(countries);
    }//GEN-LAST:event_btnCalculateActionPerformed


