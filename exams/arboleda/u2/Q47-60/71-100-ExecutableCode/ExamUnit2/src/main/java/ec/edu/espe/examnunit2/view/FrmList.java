    private void btnReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadActionPerformed
        String collectionComputers = "Computers";
        MongoCollection<org.bson.Document> mongoCollectionStudents = MongoDBUtils.accessToCollections(dataBase, collectionComputers);
        List<Document> students = MongoDBUtils.getAllCollection(mongoCollectionStudents);

        for (Document std : students) {
            mt.addRow(new Object[]{std.get("id"), std.get("name"), std.get("processorSpeed"), std.get("ramSize")});
        }
    }//GEN-LAST:event_btnReadActionPerformed

