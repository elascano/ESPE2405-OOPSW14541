    private void btnCalculateAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateAreaActionPerformed
        double radio = Double.parseDouble(txtRadio.getText());
        double height = Double.parseDouble(txtHeight.getText());
        String result =  CylinderAreaController.computeCylinderArea(radio, height);
        
        txtResult.setText(String.valueOf(result));
        double area = Double.parseDouble(result);
        
        Cylinder cylinder = new Cylinder(radio, height, area);
        utils.MongoDBUtil.create(cylinder);
       

    }//GEN-LAST:event_btnCalculateAreaActionPerformed

    private void txtRadioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRadioKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c) && c != ',' && c != '.' && c != java.awt.event.KeyEvent.VK_BACK_SPACE && c != java.awt.event.KeyEvent.VK_DELETE) {
            evt.consume();
        }
    }//GEN-LAST:event_txtRadioKeyTyped

