    private void btmCalculateDistanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmCalculateDistanceActionPerformed
        try {
            String speedText = lblSpeed.getText().trim();
            String timeText = lblTime.getText().trim();

            if (speedText.isEmpty() || timeText.isEmpty()) {
                throw new NumberFormatException("All fields must be filled.");
            }

            float speed = Float.parseFloat(speedText);
            float time = Float.parseFloat(timeText);

            if (time <= 0) {
                throw new NumberFormatException("Time must be a positive number.");
            }

            Distance distance = DistanceCalculation.calculateDistance(speed, time);
            float distanceValue = Math.abs(distance.getValue()); // Ensure distance is positive

            lblDistance.setText("The distance is: " + distanceValue + " meters.");

            MongoDBUtil.saveDistanceData(speedText, timeText, distanceValue);

        } catch (NumberFormatException e) {
            lblDistance.setText("Error: " + e.getMessage());
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), " ", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            lblDistance.setText("Database Error: " + e.getMessage());
            javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btmCalculateDistanceActionPerformed

