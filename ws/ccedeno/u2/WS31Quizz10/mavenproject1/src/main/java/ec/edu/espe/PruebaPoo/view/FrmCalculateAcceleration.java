    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        String fspeedF = txtSpeedFinal.getText();
        String fspeedI = txtSpeedInitial.getText();
        String ftime = txtTime.getText();
        
        float speedF = Float.parseFloat(fspeedF);
        float speedI = Float.parseFloat(fspeedI);
        float time = Float.parseFloat(ftime);
        
        calculateAcceleration(speedF,speedI,time);
        
        String accelerationString = String.valueOf(calculateAcceleration(speedF,speedI,time));
        
        txtAcceleration.setText(accelerationString);
        
        float accelerationStringF = Float.parseFloat(accelerationString);
        
        Car car = new Car(speedF,speedI,time,accelerationStringF);
        
        saveCar(car);
        
    }//GEN-LAST:event_btnCalculateActionPerformed

