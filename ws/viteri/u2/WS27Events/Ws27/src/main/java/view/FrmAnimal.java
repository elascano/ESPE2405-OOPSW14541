    private void ButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAddActionPerformed
        // TODO add your handling code here:
    int id;
    String description;
    Date bornOnDate;
    String gender;
    String cage;
    boolean vertebrate;
    int numberOfBones;
    List<String> food =new ArrayList<>();
    
        id=Integer.parseInt(txtId.getText());
        description= taDescription.getText();
        bornOnDate= new Date();
        gender= cmbGender.getSelectedItem().toString();
        cage= cmbCage.getSelectedItem().toString();
        numberOfBones=sliderNumberOfBones.getValue();
        vertebrate=chkVertebrate.isSelected();
        food= LstFood.getSelectedValuesList();
        
        Animal animal = new Animal(id, description, bornOnDate, gender, cage, vertebrate, numberOfBones,(ArrayList<String>) food);
        JOptionPane.showMessageDialog(this,animal.getId()+animal.getDescription(), "Registering animals", HEIGHT);
        if(JOptionPane.showConfirmDialog(this, "are you ready to save this animal=====>?" +animal.getDescription())==0){
        AnimalController.create(animal);
    }   
        
    }//GEN-LAST:event_ButtonAddActionPerformed

