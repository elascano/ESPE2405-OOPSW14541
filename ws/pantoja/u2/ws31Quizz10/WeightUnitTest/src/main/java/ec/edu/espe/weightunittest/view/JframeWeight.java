    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        try {
            double mass = Double.parseDouble(txtMass.getText());
            double gravity = 9.81;
            ControllerWeight controller = new ControllerWeight();
            double weightValue = controller.calculateWeight(mass, gravity);
            Weight weight = new Weight(mass, gravity, weightValue);
            MongoDbManager.create(weight);

            // Formatear el resultado a dos decimales
            DecimalFormat df = new DecimalFormat("#.##");
            lblResult.setText("Weight: " + df.format(weightValue) + "N");

            JOptionPane.showMessageDialog(this, "Weight saved successfully!", "Save Weight", HEIGHT);
        } catch (NumberFormatException ex) {
            lblResult.setText("Please enter a valid number for mass.");
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

