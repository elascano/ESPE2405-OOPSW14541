    private void btmComputeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmComputeActionPerformed
        try {
            float force = Float.parseFloat(txfForce.getText());
            float mass = Float.parseFloat(txfMass.getText());

            if (force < 0F || mass < 0F) {
                JOptionPane.showMessageDialog(rootPane, "Invalid entry. Please only enter positive numbers.");
            } else {
                float aceleration = ComputeAcelearation.computeAceleration(force, mass);

                Aceleration computedAceleration = new Aceleration(force, mass, aceleration);
                CloudController.create(computedAceleration);

                JOptionPane.showMessageDialog(rootPane, "The value of the aceleration is " + aceleration + " m/s^2 \nSaved in the cloud.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Invalid entry. Please only enter positive numbers.");
        }
    }//GEN-LAST:event_btmComputeActionPerformed

