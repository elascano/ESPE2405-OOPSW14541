    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Animal animal;
        int id;
        String description;
        Date bornOnDate;
        String gender;
        String cage;
        int numberOfBones;
        boolean vetebrate;
        List<String> food = new ArrayList<>();
        
        id = Integer.parseInt(ingresaId.getText());
        description = ingresadescripcion.getText();
        bornOnDate = new Date();
        gender = ingresagenero.getSelectedItem().toString();
        cage = ingresajaula.getSelectedItem().toString();
        numberOfBones = numerohuesos.getValue();
        vetebrate = esvertebrado.isSelected();
        food = ingresacomida.getSelectedValuesList();
        
        animal = new Animal(id, description, bornOnDate, gender, cage, numberOfBones, vetebrate, (ArrayList<String>) food);
        
        JOptionPane.showMessageDialog(this, animal.getId() + animal.getDescription() + animal.getFoods(), "Registering Animals", HEIGHT);
        if(JOptionPane.showConfirmDialog(this, "are you sure you want to save the animal --> " + animal.getDescription()) == 0){
            AnimalControler.create(animal);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

