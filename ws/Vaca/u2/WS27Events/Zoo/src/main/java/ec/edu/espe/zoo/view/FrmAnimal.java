    private void RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterActionPerformed
        int id;
        String   description;
        Date bornOnDate;
        String gender;
        String cage;
        int numberOfBones;
        boolean vertebrate;
        List<String> foods= new ArrayList<>();
        
        id = Integer.parseInt(txtId.getText());
        description = Description.getText();
        bornOnDate = new Date();
        gender = cmbGender.getSelectedItem().toString();
        cage = cmbCage.getSelectedItem().toString();
        numberOfBones = sldNumberOfBones.getValue();
        vertebrate = jCheckBox1.isSelected();
        foods = jList1.getSelectedValuesList();
        
        Animal animal = new Animal(id, description, bornOnDate, gender, cage, numberOfBones, vertebrate, (ArrayList<String>) foods);
        
        //JOptionPane.showMessageDialog(this, animal.getId() + animal.getDescription() + animal.getFoods(), "Registering Animals", HEIGHT);
        if(JOptionPane.showConfirmDialog(this, "are you sure to save animal -->" + animal.getDescription()) == 0){
            AnimalController.create(animal);
        }
    }//GEN-LAST:event_RegisterActionPerformed

